    <h3>Padded Envelopes</h3>
    Bubble Envelopes are envelopes lined with bubblewrap ensuring maximum protection for its contents. Bubble envelopes are often used to post bulky but delicate items rather than just letters. These are perfect for eBay packaging.
    Jiffy Bags are rugged, all-purpose envelopes. They are paper fiber cushioned, constructed from a heavy-duty kraft with a   uniform inner batting (padding) that provides excellent cushioning   protection.
    <h3>Board Backed Envelopes</h3>
    Board Backed Envelopes have one side made of a thick card, this offers protection from bending and damage, they keep fragile or important documents safe from damage or wear.
    <h3>Gusset Envelopes</h3>
    Gusset envelopes are envelopes with an expandable &quot;V&quot; shaped sides which massivley increase the capacity of the envelope. Avaliable in a range of sizes our gusset envelopes are made of a paper thickness of 115-155gsm, therefore they are strong with a large capacity ideal for sending milti-page documents or catalogues and other bulky items, all our gusset envelops are &quot;peel and seal&quot;.</p>
    <em>We also offer an extensive range machine envelopes, wage envelopes, CD, plastic and other types of envelope.</em>
    <h3>Gummed Envelopes</h3>
    The most traditional and best value way of sealing an envelope. The glue on the flap of the envelope must be moistened by licking or a sponge and sealed. 
    <h3>Self Sealing Envelopes</h3>
    As the most common way of sealing a business envelope there is a strip of glue on the flap and envelope which seal when pressed together. This is slightly more expensive than gummed envelopes however it is more convenient and forms a tighter bond.
    <h3>Peel and Seal Envelopes</h3>
    With a peel and seal envelope the strip of glue on the flap is protected by a plastic strip which is removed and disposed of on sealing. The strongest bond is formed with peel and seal and the price is often similar to self sealing envelopes.
    <h3>Windowed Envelopes</h3>
    Windowed envelopes allow you to display the address though the envelope saving time as the address only needs to appear on the letter. Windowed envelopes also look more professional and are often the envelope of choice for all correspondence
    <h3>Plain Envelopes</h3>
    Plain envelopes allow you to write or print the address on the envelope, this allows you to add a parsonal touch however this can look unprofessional in business situations.
    <h3>Weight</h3>
  	The thickness of the paper used to make the envelope is measured the same way as paper, using ‘gsm’ (Grams per square   metre). For most uses 80-90gsm is used as it is best value however for   more formal or important documents you may wish to use 100-125gsm   envelopes as they will feel like better quality to the person receiving   the letter.
    <h3>Privacy</h3>
    If the letter being sent is private or contains sensetive information you may wish to use opaque envelopes to ensure the writing on the inside cannot be read from outside the envelope. This is a cheived a a pattern or colour being printed on the inside of the envelope.
